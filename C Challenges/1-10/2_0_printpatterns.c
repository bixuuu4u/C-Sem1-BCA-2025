// Show   patterns just using print statements
#include <stdio.h>

int main()
{

    printf("Right Half Pyramid\n");
    printf("*");
    printf("\n* *");
    printf("\n* * *");
    printf("\n* * * *");
    printf("\n* * * * *");

    printf("\nReverse Right Half Pyramid\n");
    printf("\n* * * * *");
    printf("\n* * * *");
    printf("\n* * *");
    printf("\n* *");
    printf("\n*");

    printf("\nLeft Half Pyramid");
    printf("\n        *");
    printf("\n      * *");
    printf("\n    * * *");
    printf("\n  * * * *");
    printf("\n* * * * *");

    return 0;
}
