// Show   patterns just using single print statements
#include <stdio.h>

int main()
{
    printf("Right Half Pyramid\n");

    printf("*\n* *\n* * *\n* * * *\n* * * * *");
    printf("\nReverse Right Half Pyramid\n");

    printf("\n* * * * *\n* * * *\n* * *\n* *\n*");
    printf("\nLeft Half Pyramid");

    printf("\n        *\n      * *\n    * * *\n  * * * *\n* * * * *");

    return 0;
}
