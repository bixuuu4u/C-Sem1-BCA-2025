

#include <stdio.h>

int main()
{
    int a = 5;
    // Defn betn ++a and a++ ,, --a and a--
    printf("a=%d \n", a);
    printf("++a=%d \n", ++a);
    printf("a++=%d\n", a++);
    printf("a=%d\n", a);
    printf("___________________________________\n");
    int b = 5;
    // Defn betn ++a and a++ ,, --a and a--
    printf("b=%d \n", b);
    printf("--b=%d \n", --b);
    printf("b--=%d\n", b--);
    printf("b=%d\n", b);
    return 0;
}